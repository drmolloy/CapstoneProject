public class Team
{
    private String name;
    private int wins;
    
    /**
     * Constructs a team object
     * @param aName the name of the team
     * @param aWins the number of wins the team has
     */
    public Team(String aName, int aWins)
    {
        name = aName;
        wins = aWins;
    }
}
